
public class Vertex {
	int node;
	int x;
	int y;
	public Vertex(int node, int x, int y){
		this.node = node;
		this.x=x;
		this.y=y;
	}
	public int getNode() {
		return node;
	}
	public void setNode(int node) {
		this.node = node;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
